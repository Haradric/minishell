#include "minishell.h"

void	builtin_echo(int argc, char **argv)
{
	int		i;

	i = 1;
	while (i < argc)
	{
		if (i != 1)
			write(1, " ", 1);
		ft_printf("%s", argv[i]);
		i++;
	}
	write(1, "\n", 1);
}
